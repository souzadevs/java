package model;

/**
 *
 * @author Bruno Araujo
 */
public class ProductModel
{
    private String name;
    private Double price;

    public ProductModel()
    {
        
    }
    
    public ProductModel(String name, Double price)
    {
        this.name = name;
        this.price = price;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public Double getPrice()
    {
        return price;
    }

    public void setPrice(Double price)
    {
        this.price = price;
    }
}
